package com.example.bookshop.bookshop.modul;

/**
 *
 * @author nur13
 */
public class Tip {
    
    private int typeId;
    private String type;

    public Tip() {
    }

    public Tip(int typeId, String type) {
        this.typeId = typeId;
        this.type = type;
    }

    public int getTypeId() {
        return typeId;
    }

    public void setTypeId(int typeId) {
        this.typeId = typeId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return type ;
    }
    
    
}
